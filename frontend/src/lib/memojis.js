// All banks combined for lookup
const ALL_BANKS = [...KIDS_BANK, ...TEEN_BANK, ...YA_BANK];

// Legacy compat
export const MEMOJI_BANK = KIDS_BANK;

export function getBankForAge(ageGroup) {
  if (ageGroup === '8-10' || ageGroup === '11-13') return KIDS_BANK;
  if (ageGroup === '14-16') return TEEN_BANK;
  return YA_BANK; // 17-20 or default
}

export function getMemoji(name, ageGroup) {
  const bank = getBankForAge(ageGroup);
  if (!name) return bank[0].src;
  let hash = 0;
  for (let i = 0; i < name.length; i++) {
    hash = name.charCodeAt(i) + ((hash << 5) - hash);
  }
  return bank[Math.abs(hash) % bank.length].src;
}

export function getMemojiById(id) {
  const found = ALL_BANKS.find(m => m.id === id);
  return found ? found.src : KIDS_BANK[0].src;
}
