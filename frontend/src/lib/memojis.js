function buildBank(prefix, boySrcs, girlSrcs) {
  const bank = [];
  boySrcs.forEach((src, i) => {
    bank.push({ id: `${prefix}-b${i + 1}`, src, label: `Boy ${i + 1}`, gender: 'boy' });
  });
  girlSrcs.forEach((src, i) => {
    bank.push({ id: `${prefix}-g${i + 1}`, src, label: `Girl ${i + 1}`, gender: 'girl' });
  });
  return bank;
}

